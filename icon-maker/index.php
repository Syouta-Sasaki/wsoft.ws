	<h1 class="auto-style1">アイコンメーカー</h1>
	<img class="img-style1" src="screenshot.png"></img>
	<p class="auto-style3">・はじめに
自作ソフトなどのアイコン画像を描いて(あるいは描いてもらって)その画像をそのままアイコンファイル(*.ico)に変換できる軽量なソフトです
GIMPなどの大規模なプログラムを使用せずとも簡単に画像ファイルをアイコンファイルに変換することができます。

・使用方法
　・アイコンメーカーを開いて使う
　　1.アイコンメーカー.exeを起動します
　　2.アイコンファイルの形式、サイズをドロップダウンリストから選択し、ファイルをドラッグアンドドロップして生成します

　・プログラムにドラッグアンドドロップして使う
　　アイコンメーカー.exeに対応する形式の画像ファイルをドラッグアンドドロップするとアイコンファイルが生成されます
　　プログラムの挙動は「設定」で変更することができます

・設定
　設定を変更すると、アイコンメーカー.iniが自動的に生成されます
　・ドラッグアンドドロップされた画像をアイコンに変換する
　　規定値は「はい」です。プログラムにドラッグアンドドロップされた場合の動作可否を設定します
　・アイコンの保存先
　　規定値は「画像ファイルと同じディレクトリ」です。次のオプションが選択できます
　　・「画像ファイルと同じディレクトリ」ドラッグアンドドロップされた画像ファイルと同じディレクトリに生成します
　　・「実行ファイルと同じディレクトリ」アイコンメーカー.exeがあるディレクトリに生成します
　　・自由選択　ドロップダウンリスト右の[...]をクリックすることで直接ディレクトリを指定できます
　・アイコンタイプ,サイズ
　　開いて使った時と同じ動作です。アイコンの出力形式とサイズを指定します

・技術情報
　アイコンメーカー.iniの正確なファイル名は「{アイコンメーカーの実行ファイルの名前}.ini」です</p>
